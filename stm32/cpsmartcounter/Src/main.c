/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
  initialize();
  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_ADC2_Init();
  MX_ADC1_Init();
  MX_TIM3_Init();
  /* USER CODE BEGIN 2 */
//  HAL_TIM_Base_Start_IT(&htim3);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  sensor_state = get_input();

	  if(start_input != 1)
		  continue;

	  change_state(sensor_state);

	  if(huart2.RxState == HAL_UART_STATE_READY)
	  {
		  GPIOD->ODR ^= (1<<13);
		  if(HAL_UART_Receive(&huart2, uart_in, 1, 100) == HAL_OK)
		  {
			  GPIOD->ODR ^= (1<<12);
			  change_status(uart_in[0]);
		  }
	  }

	  check_reset();
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
//	  HAL_Delay(DELAY);
  }
  /* USER CODE END 3 */

}

