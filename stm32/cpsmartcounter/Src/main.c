/* USER CODE BEGIN 0 */
void initialize()
{
	// transition[old_state][new_state] = 1;	-- if not possible -- 0
	// condition[old_state][new_state] = input	-- input = 2*in_state + out_state
	// output[old_state][new_state] = +1 (increment) or -1 (decrement)

	current_state = 0;

	transition[0][1] = 1;
	condition[0][1] = 1;
	output[0][1] = 0;

	transition[0][4] = 1;
	condition[0][4] = 2;
	output[0][4] = 0;

	transition[0][7] = 1;
	condition[0][7] = 3;
	output[0][7] = 0;

	transition[1][2] = 1;
	condition[1][2] = 3;	// or 0 -- check in change_state(uint8_t, uint8_t)
	output[1][2] = 0;

	// ADDED
	transition[1][3] = 1;
	condition[1][3] = 2;
	output[1][3] = 0;

//	transition[2][0] = 1;
//	condition[2][0] = 0;
//	output[2][0] = +1;

	transition[2][3] = 1;
	condition[2][3] = 2;
	output[2][3] = 0;

	transition[3][0] = 1;
	condition[3][0] = 0;
	output[3][0] = +1;

	transition[4][5] = 1;
	condition[4][5] = 3;	// or 0 -- check in change_state(uint8_t, uint8_t)
	output[4][5] = 0;

	// ADDED
	transition[4][6] = 1;
	condition[4][6] = 1;
	output[4][6] = 0;

//	transition[5][0] = 1;
//	condition[5][0] = 0;
//	output[5][0] = -1;

	transition[5][6] = 1;
	condition[5][6] = 1;
	output[5][6] = 0;

	transition[6][0] = 1;
	condition[6][0] = 0;
	output[6][0] = -1;

	transition[7][0] = 1;
	condition[7][0] = 0;
	output[7][0] = 0;

	transition[7][3] = 1;
	condition[7][3] = 2;
	output[7][3] = 0;

	transition[7][6] = 1;
	condition[7][6] = 1;
	output[7][6] = 0;
}

uint8_t get_input()
{
	HAL_ADC_Start(&hadc1);
	HAL_ADC_Start(&hadc2);

	uint32_t inside = HAL_ADC_GetValue(&hadc1);
	uint32_t outside = HAL_ADC_GetValue(&hadc2);

	sm_in += (inside - in_buf[idx]);
	sm_out += (outside - out_buf[idx]);

	in_buf[idx] = inside;
	out_buf[idx] = outside;

	++idx;

	if(idx == WINDOW)
	{
		start_input = 1;
		idx -= WINDOW;
	}

	if(debug == 1)
	{
		if(cnt >= 2*MAX_TIME)
		{
			cnt -= 2*MAX_TIME;
			char str[35];
			sprintf(str, "%4d %4d: %d |", (int)(sm_in / WINDOW), (int)(sm_out / WINDOW), current_state);
			HAL_UART_Transmit(&huart2, str, 12, 1000);
		}
		++cnt;
	}

	inside = (sm_in / WINDOW < THRESHOLD1? 1: 0);
	outside = (sm_out / WINDOW < THRESHOLD2? 1: 0);

	return 2*inside + outside;
}

void change_state(uint8_t input)
{
	int i;
	for(i = 0; i < 8; ++i)
	{
		if(transition[current_state][i] == 1 && input == condition[current_state][i])
		{
			GPIOD->ODR ^= (1<<15);
			send_data(output[current_state][i]);
			current_state = i;
			remaining_time = MAX_TIME;
			return;
		}
	}

	if(current_state == 1 && input == 0)
	{
		GPIOD->ODR ^= (1<<15);
		send_data(output[1][2]);
		current_state = 2;
		remaining_time = MAX_TIME;
	}

	if(current_state == 4 && input == 0)
	{
		GPIOD->ODR ^= (1<<15);
		send_data(output[4][5]);
		current_state = 5;
		remaining_time = MAX_TIME;
	}
}

void send_data(int8_t value)
{
	if(value == 0)
		return;


	if(remaining_gap > 0)
		return;

	GPIOD->ODR ^= (1<<14);

	if(debug == 0)
	{
		if(value == 1)
			HAL_UART_Transmit(&huart2, "I", 1, 100);
		else
			HAL_UART_Transmit(&huart2, "D", 1, 100);
	}

	remaining_gap = MIN_GAP;
}

void change_status(uint8_t count)
{
	if(count > 0)
		HAL_GPIO_WritePin(GPIOB, GPIO_PIN_11, 1);
	else
		HAL_GPIO_WritePin(GPIOB, GPIO_PIN_11, 0);

	if(count > 2)
		HAL_GPIO_WritePin(GPIOB, GPIO_PIN_13, 1);
	else
		HAL_GPIO_WritePin(GPIOB, GPIO_PIN_13, 0);

	if(count > 5)
		HAL_GPIO_WritePin(GPIOB, GPIO_PIN_15, 1);
	else
		HAL_GPIO_WritePin(GPIOB, GPIO_PIN_15, 0);
}

void check_reset()
{
	if(current_state == 0)
	{
		remaining_time = MAX_TIME;
		return;
	}

	--remaining_time;
	if(remaining_time <= 0)
	{
		current_state = 0;
		remaining_time = MAX_TIME;
	}

	--remaining_gap;
	if(remaining_gap <= 0)
		remaining_gap = 0;
}
